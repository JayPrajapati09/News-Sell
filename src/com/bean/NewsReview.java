package com.bean;

import java.sql.Date;

public class NewsReview {
	
	int id, nid, bid, news_relevant, content_quality, media_quality;
	



	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNid() {
		return nid;
	}

	public void setNid(int nid) {
		this.nid = nid;
	}

	public int getBid() {
		return bid;
	}

	public void setBid(int bid) {
		this.bid = bid;
	}

	public int getNews_relevant() {
		return news_relevant;
	}

	public void setNews_relevant(int news_relevant) {
		this.news_relevant = news_relevant;
	}

	public int getContent_quality() {
		return content_quality;
	}

	public void setContent_quality(int content_quality) {
		this.content_quality = content_quality;
	}

	public int getMedia_quality() {
		return media_quality;
	}

	public void setMedia_quality(int media_quality) {
		this.media_quality = media_quality;
	}
	

}
